import turtle
t = turtle.Turtle()
while True:
    t.speed(10000)  # Set the speed to the fastest
    t.forward(100)
    t.right(90)
    t.forward(100)
    t.right(90)
    t.forward(100)
    t.right(90)
    t.forward(100)
    t.right(90)
    t.right(1)  # Rotate the square slightly to create a spiral effect
    t.right(1)  # Rotate the square slightly to create a spiral effect